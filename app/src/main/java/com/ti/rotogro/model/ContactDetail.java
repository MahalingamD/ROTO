package com.ti.rotogro.model;

public class ContactDetail {

   private String myContactName = "";
   private String myContactAddress = "";
   private String myContactMobile = "";

   public String getMyContactName() {
      return myContactName;
   }

   public void setMyContactName( String myContactName ) {
      this.myContactName = myContactName;
   }

   public String getMyContactAddress() {
      return myContactAddress;
   }

   public void setMyContactAddress( String myContactAddress ) {
      this.myContactAddress = myContactAddress;
   }

   public String getMyContactMobile() {
      return myContactMobile;
   }

   public void setMyContactMobile( String myContactMobile ) {
      this.myContactMobile = myContactMobile;
   }
}
